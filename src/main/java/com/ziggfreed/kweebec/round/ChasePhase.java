package com.ziggfreed.kweebec.round;

/**
 * The three beats of a Chase round. PREP gives the party a breath to scatter,
 * RITUAL is the shrine-relight loop while corruption climbs, ESCAPE is the climax:
 * a capstone boss may first BAR the Heartwood Gate (defeat it to open it), then the
 * gate opens and the hunter locks onto the nearest survivor for the run out.
 */
public enum ChasePhase {
    PREP,
    RITUAL,
    ESCAPE
}
